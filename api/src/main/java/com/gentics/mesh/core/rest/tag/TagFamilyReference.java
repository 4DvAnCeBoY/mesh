package com.gentics.mesh.core.rest.tag;

public class TagFamilyReference {

	private String name;
	private String uuid;

	public TagFamilyReference() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

}
